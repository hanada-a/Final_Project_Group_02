    private void btnFulfillPrescriptionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFulfillPrescriptionActionPerformed
        
        int selectedRow = tblWorkRequests.getSelectedRow();
        
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this,"Select a prescription to fulfill");
            return;
        }
        
        String notes = txtFulfillmentNotes.getText().trim();
        if (notes.isEmpty()) {
            JOptionPane.showMessageDialog(this,"Enter fulfillment notes before processing");
            return;
        }
        
        
        DefaultTableModel model = (DefaultTableModel) tblWorkRequests.getModel();
        String patientName = (String) model.getValueAt(selectedRow, 0);
        PrescriptionRequest selectedRequest = null;
        
        for (WorkRequest request : organization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof PrescriptionRequest) {
                PrescriptionRequest rxReq = (PrescriptionRequest) request;
                if (rxReq.getPatientName().equals(patientName) && rxReq.getReceiver().equals(userAccount)) {
                    selectedRequest = rxReq;
                    break;
                }
            }
        }
        
        
        if (selectedRequest != null) {
            selectedRequest.setStatus("Fulfilled");
            selectedRequest.setFulfillmentNotes(notes);
            selectedRequest.setResolveDate(new java.util.Date());
            
            JOptionPane.showMessageDialog(this,"Prescription fulfilled");
            
            
            txtFulfillmentNotes.setText("");
            populateRequestTable();
        }
        
    }//GEN-LAST:event_btnFulfillPrescriptionActionPerformed

